package kr.co.lcalmsky.macro.vo;

public class Settings {

	private int	emptyX;
	private int	emptyY;
	private int	timeX;
	private int	timeY;
	private int	startX;
	private int	startY;

	public int getEmptyX() {
		return emptyX;
	}

	public void setEmptyX(int emptyX) {
		this.emptyX = emptyX;
	}

	public int getEmptyY() {
		return emptyY;
	}

	public void setEmptyY(int emptyY) {
		this.emptyY = emptyY;
	}

	public int getTimeX() {
		return timeX;
	}

	public void setTimeX(int timeX) {
		this.timeX = timeX;
	}

	public int getTimeY() {
		return timeY;
	}

	public void setTimeY(int timeY) {
		this.timeY = timeY;
	}

	public int getStartX() {
		return startX;
	}

	public void setStartX(int startX) {
		this.startX = startX;
	}

	public int getStartY() {
		return startY;
	}

	public void setStartY(int startY) {
		this.startY = startY;
	}
}